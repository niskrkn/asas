<?php
// Заданные числа для сравнения
$number1 = 10;
$number2 = 20;
$number3 = 15;

// Находим наибольшее число
$max_number = max($number1, $number2, $number3);

// Находим наименьшее число
$min_number = min($number1, $number2, $number3);

// Выводим результат
echo "Наибольшее число: $max_number<br>";
echo "Наименьшее число: $min_number";
?>